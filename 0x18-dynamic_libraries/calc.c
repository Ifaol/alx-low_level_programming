#include <stdio.h>
/**
*add - addition function
*@a: int input
*@b: int input
*
*Return: result int
*/
int add(int a, int b)
{
return (a + b);
}
/**
*sub - addition function
*@a: int input
*@b: int input
*
*Return: result int
*/
int sub(int a, int b)
{
return (a - b);
}
/**
*mul - multiplication function
*@a: int input
*@b: int input
*
*Return: result int
*/
int mul(int a, int b)
{
return (a * b);
}
/**
*div - addition function
*@a: int input
*@b: int input
*
*Return: result int
*/
int div(int a, int b)
{
return (a / b);
}
/**
*mod - modules function
*@a: int input
*@b: int input
*
*Return: result int
*/
int mod(int a, int b)
{
return (a % b);
}
